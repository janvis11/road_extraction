dice_loss = smp.losses.DiceLoss(mode="binary", from_logits=True, smooth=1e-6)
bce_loss = nn.BCEWithLogitsLoss()

def combined_loss(logits, target):
    return 0.7 * dice_loss(logits, target) + 0.3 * bce_loss(logits, target)

optimizer = torch.optim.AdamW(model.parameters(), lr=2e-4, weight_decay=1e-4)
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=12, eta_min=1e-6)

