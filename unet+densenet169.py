def move_all_files(src_dir, dst_dir):
    safe_mkdir(dst_dir)

    moved = 0
    skipped = 0

    for file_path in sorted(src_dir.glob("*")):
        if not file_path.is_file():
            continue

        dst_path = dst_dir / file_path.name

        if dst_path.exists():
            print(f"Skipping existing file: {dst_path.name}")
            skipped += 1
            continue

        shutil.move(str(file_path), str(dst_path))
        moved += 1

    return moved, skipped

