def find_dataset_root(root):
    candidates = [root]
    for item in os.listdir(root):
        p = os.path.join(root, item)
        if os.path.isdir(p):
            candidates.append(p)

    for c in candidates:
        if (
            os.path.isdir(os.path.join(c, "train", "image")) and
            os.path.isdir(os.path.join(c, "train", "mask")) and
            os.path.isdir(os.path.join(c, "val", "image")) and
            os.path.isdir(os.path.join(c, "val", "mask"))
        ):
            return c

    raise FileNotFoundError(
        "Could not find dataset root containing train/image, train/mask, val/image, val/mask"
    )

