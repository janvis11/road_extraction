def tensor_to_display_rgb(img_chw):
    x = img_chw.detach().cpu().numpy()
    x = np.transpose(x, (1, 2, 0))
    x = x[:, :, :3]

    out = np.zeros_like(x, dtype=np.float32)
    for c in range(x.shape[-1]):
        ch = x[:, :, c]
        mn, mx = ch.min(), ch.max()
        if mx - mn < 1e-6:
            out[:, :, c] = 0.0
        else:
            out[:, :, c] = (ch - mn) / (mx - mn)
    return np.clip(out, 0, 1)


