def init_metric_sums():
    return {
        "tp": 0,
        "fp": 0,
        "fn": 0
    }

def update_metric_sums(metric_sums, pred, gt):
    pred = pred.bool()
    gt = gt.bool()

    metric_sums["tp"] += torch.logical_and(pred, gt).sum().item()
    metric_sums["fp"] += torch.logical_and(pred, ~gt).sum().item()
    metric_sums["fn"] += torch.logical_and(~pred, gt).sum().item()

def compute_metrics(metric_sums):
    tp = metric_sums["tp"]
    fp = metric_sums["fp"]
    fn = metric_sums["fn"]

    eps = 1e-7

    iou = tp / (tp + fp + fn + eps)
    precision = tp / (tp + fp + eps)
    recall = tp / (tp + fn + eps)
    f1 = (2 * tp) / (2 * tp + fp + fn + eps)

    return iou, f1, precision, recall

